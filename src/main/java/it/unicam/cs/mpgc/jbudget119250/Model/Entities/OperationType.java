package it.unicam.cs.mpgc.jbudget119250.Model.Entities;

/**
 * Represents the type of operation associated with a financial movement.
 * This enumeration defines two possible types of operations:
 * - INCOME: Indicates a financial movement that adds to the financial balance.
 * - EXPENSE: Indicates a financial movement that subtracts from the financial balance.
 */
public enum OperationType {
    INCOME, EXPENSE
}
